def remove_slide_shapes(slide) -> None:
    """Keep slide background/theme, but remove old sample content."""
    for shape in list(slide.shapes):
        slide.shapes._spTree.remove(shape._element)


def add_text(
    slide,
    text: str,
    left: float,
    top: float,
    width: float,
    height: float,
    *,
    font_size: int = 20,
    bold: bool = False,
    color: RGBColor = DARK,
    align: PP_ALIGN | None = None,
) -> None:
    box = slide.shapes.add_textbox(Inches(left), Inches(top), Inches(width), Inches(height))
    tf = box.text_frame
    tf.clear()
    tf.word_wrap = True
    p = tf.paragraphs[0]
    p.text = text
    if align is not None:
        p.alignment = align
    for run in p.runs:
        run.font.name = "Times New Roman"
        run.font.size = PptPt(font_size)
        run.font.bold = bold
        run.font.color.rgb = color


def add_bullets(
    slide,
    bullets: list[str],
    left: float,
    top: float,
    width: float,
    height: float,
    *,
    font_size: int = 18,
    color: RGBColor = DARK,
) -> None:
    box = slide.shapes.add_textbox(Inches(left), Inches(top), Inches(width), Inches(height))
    tf = box.text_frame
    tf.clear()
    tf.word_wrap = True
    for idx, bullet in enumerate(bullets):
        p = tf.paragraphs[0] if idx == 0 else tf.add_paragraph()
        p.text = bullet
        p.level = 0
        p.font.name = "Times New Roman"
        p.font.size = PptPt(font_size)
        p.font.color.rgb = color


def add_title(slide, number: int, title: str) -> None:
    add_text(slide, str(number), 0.25, 0.15, 0.45, 0.3, font_size=14, bold=True, color=GRAY)
    add_text(slide, title, 0.75, 0.12, 11.3, 0.55, font_size=26, bold=True, color=BLUE)
    line = slide.shapes.add_shape(1, Inches(0.75), Inches(0.72), Inches(11.0), Inches(0.03))
    line.fill.solid()
    line.fill.fore_color.rgb = BLUE
    line.line.color.rgb = BLUE


def add_table(
    slide,
    rows: list[list[str]],
    left: float,
    top: float,
    width: float,
    height: float,
    *,
    font_size: int = 12,
    header_fill: RGBColor = LIGHT_BLUE,
) -> None:
    table_shape = slide.shapes.add_table(
        len(rows), len(rows[0]), Inches(left), Inches(top), Inches(width), Inches(height)
    )
    table = table_shape.table
    for r, row in enumerate(rows):
        for c, value in enumerate(row):
            cell = table.cell(r, c)
            cell.text = value
            cell.margin_left = Inches(0.04)
            cell.margin_right = Inches(0.04)
            cell.margin_top = Inches(0.02)
            cell.margin_bottom = Inches(0.02)
            fill = cell.fill
            fill.solid()
            fill.fore_color.rgb = header_fill if r == 0 else RGBColor(255, 255, 255)
            for p in cell.text_frame.paragraphs:
                p.alignment = PP_ALIGN.CENTER if r == 0 else PP_ALIGN.LEFT
                for run in p.runs:
                    run.font.name = "Times New Roman"
                    run.font.size = PptPt(font_size)
                    run.font.bold = r == 0
                    run.font.color.rgb = DARK


def add_bar_chart(
    slide,
    categories: list[str],
    values: list[float],
    left: float,
    top: float,
    width: float,
    height: float,
    *,
    title: str,
    number_format: str = "0.0",
) -> None:
    chart_data = CategoryChartData()
    chart_data.categories = categories
    chart_data.add_series(title, values)
    chart = slide.shapes.add_chart(
        XL_CHART_TYPE.COLUMN_CLUSTERED,
        Inches(left),
        Inches(top),
        Inches(width),
        Inches(height),
        chart_data,
    ).chart
    chart.has_legend = False
    chart.value_axis.has_major_gridlines = True
    chart.value_axis.tick_labels.font.size = PptPt(9)
    chart.category_axis.tick_labels.font.size = PptPt(10)
    plot = chart.plots[0]
    plot.has_data_labels = True
    plot.data_labels.position = XL_LABEL_POSITION.OUTSIDE_END
    plot.data_labels.number_format = number_format
    plot.data_labels.font.size = PptPt(9)
    chart.series[0].format.fill.solid()
    chart.series[0].format.fill.fore_color.rgb = BLUE


