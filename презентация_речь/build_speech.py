# -*- coding: utf-8 -*-
"""
Формирование текста защитной речи (доклада) к ВКР Кострова.

Структура доклада повторяет шаблон «речь.doc»:
приветствие → тема и актуальность → объект → предмет → цель → задачи →
база исследования → основные результаты по главам → практическая значимость →
вывод → «Спасибо за внимание».

Объём — около 3 страниц, ориентировочно 7 минут выступления.
Оформление: Times New Roman 14 пт, полуторный интервал, выравнивание по ширине,
абзацный отступ 1,25 см. Привязка к слайдам презентации указана курсивом.
"""
from docx import Document
from docx.shared import Pt, Cm
from docx.enum.text import WD_ALIGN_PARAGRAPH, WD_LINE_SPACING

OUT = "Речь_Костров_ВКР.docx"

doc = Document()

style = doc.styles["Normal"]
style.font.name = "Times New Roman"
style.font.size = Pt(14)
pf = style.paragraph_format
pf.line_spacing_rule = WD_LINE_SPACING.ONE_POINT_FIVE
pf.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
pf.first_line_indent = Cm(1.25)
pf.space_after = Pt(0)

for sec in doc.sections:
    sec.top_margin = Cm(2.0)
    sec.bottom_margin = Cm(2.0)
    sec.left_margin = Cm(3.0)
    sec.right_margin = Cm(1.5)


def para(text, *, bold=False, italic=False, align=WD_ALIGN_PARAGRAPH.JUSTIFY,
         indent=True, size=14, center=False):
    p = doc.add_paragraph()
    p.paragraph_format.alignment = WD_ALIGN_PARAGRAPH.CENTER if center else align
    if not indent:
        p.paragraph_format.first_line_indent = Cm(0)
    r = p.add_run(text)
    r.font.name = "Times New Roman"
    r.font.size = Pt(size)
    r.bold = bold
    r.italic = italic
    return p


def slide_para(slide_tag, text):
    """Абзац доклада с компактной пометкой о слайде в начале (курсив)."""
    p = doc.add_paragraph()
    p.paragraph_format.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
    rn = p.add_run(slide_tag + " ")
    rn.font.name = "Times New Roman"
    rn.font.size = Pt(12)
    rn.italic = True
    r = p.add_run(text)
    r.font.name = "Times New Roman"
    r.font.size = Pt(14)
    return p


